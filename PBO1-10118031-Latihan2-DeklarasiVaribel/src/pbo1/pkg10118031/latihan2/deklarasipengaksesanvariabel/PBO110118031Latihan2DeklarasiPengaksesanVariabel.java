public class PBO110118031Latihan2DeklarasiPengaksesanVariabel {

    public static void main(String[] args) {
        // Deklarasi Varibel
        int nilaiInt;
        final double PHI = 3.14; //konstanta uppercase
        boolean nilaiLogika;
        char nilaiKarakter;

        //Memberi nilai ke variabel
        nilaiInt = 78;
        nilaiLogika = false;
        nilaiKarakter = 'D';

        //Menampilkan Hasil
        System.out.println("Isi varibel nilai = " + nilaiInt);
        System.out.println("Isi varibel PHI = " + PHI);
        System.out.println("Isi varibel Logika = " + nilaiLogika);
        System.out.println("Isi varibel Karakter = " + nilaiKarakter);
    }
}
